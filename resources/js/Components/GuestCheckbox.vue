<script setup>
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        required: true,
    },
    value: {
        default: null,
    },
});

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <input
        type="checkbox"
        :value="value"
        v-model="proxyChecked"
        class="size-8 rounded border-none bg-zinc-200 text-indigo-500 hover:bg-zinc-300 checked:hover:text-indigo-600 focus:ring-0 focus:ring-offset-0 md:size-5"
    />
</template>
